namespace MinimalHtml.Vite;

/// <summary>
/// A resolver for Vite manifest files. This class is responsible for reading the Vite manifest file, caching the results, and providing methods to retrieve asset information and write import maps. The manifest file is expected to be in JSON format and should contain information about the assets generated by Vite, including their file paths, integrity hashes, and any imports or dependencies. The resolver also supports caching the manifest data in memory and invalidating the cache when the manifest file changes, allowing for efficient retrieval of asset information without needing to read the file on every request.
/// </summary>
public static class ViteManifestExtensions
{
    /// <summary>
    /// Registers the Vite manifest resolver and related services in the dependency injection container. This method adds a singleton instance of the <see cref="ViteManifestResolver"/> to the service collection, which is responsible for reading and caching the Vite manifest file. It also registers delegates for retrieving asset information and writing import maps, which can be injected into other services or used directly in endpoints. The method takes optional parameters for the manifest path and import map path, allowing for customization of where the manifest file is located and whether an import map should be used. By default, it looks for the manifest file at ".vite/manifest.json" relative to the web root.
    /// </summary>
    /// <param name="services"></param>
    /// <param name="manifestPath"></param>
    /// <param name="importmapPath"></param>
    /// <returns></returns>
    public static IServiceCollection RegisterViteAssets(this IServiceCollection services, string? manifestPath = null, string? importmapPath = null) =>
        services.AddSingleton(s => new ViteManifestResolver(
            manifestPath ?? ViteManifestResolver.DefaultRelativeManifestPath,
            importmapPath,
            s.GetRequiredService<IWebHostEnvironment>(),
            s.GetRequiredService<IMemoryCache>()))
        .AddSingleton<GetAsset>(s => s.GetRequiredService<ViteManifestResolver>().GetAsset)
        .AddSingleton<WriteImportMap>(s => s.GetRequiredService<ViteManifestResolver>().WriteImportMap);
}

